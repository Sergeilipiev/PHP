<?php
//    Чему будет равно i:
//    1) i += ++i + i++, если i = 6
//    2) ++i+i++ + i, если i = 1
//    3) b = a++ + (--a * ++a), чему равно b, если а = 2  (сделали на занятии)

$i = 6;
$i += ++$i + $i++;
echo "1) $i<br/>";

$i2 = 1;
$i2 = ++$i2+$i2++ + $i2;
echo "2) $i2<br/>";

$a = 2;
$b = $a++ + (--$a * ++$a);
echo "3) $b<br/>";
